def func(x, y):
    return 0 if x == y else 1 if x > y else -1
# 如果 x > y且x != y 返回 1   print(func(2,1)) = 1
# 如果 x < y且x != y 返回 0   print(func(1,2)) = -1
# 如果 x == y 返回 0   print(func(2,2)) = 0




